const sendMail = require("../utils/sendMail"); 

// create new order
const sendmail=async(mailId,subject,message)=>{
  try {
    await sendMail({
      email: mailId,
      subject: subject,
      message: message,
    });
  } catch (error) {
    return next(new ErrorHandler(error.message, 500));
  }
}
router.post(
  "/create-order",
  catchAsyncErrors(async (req, res, next) => {
    try {
      const { cart, shippingAddress, user, totalPrice, paymentInfo } = req.body;
      console.log('user detail from create order',user)
      //   group cart items by shopId
      const shopItemsMap = new Map();

      for (const item of cart) {
        const shopId = item.shopId;
        if (!shopItemsMap.has(shopId)) {
          shopItemsMap.set(shopId, []);
        }
        shopItemsMap.get(shopId).push(item);
      }

      // create an order for each shop
      const orders = [];

      for (const [shopId, items] of shopItemsMap) {
        const order = await Order.create({
          cart: items,
          shippingAddress,
          user,
          totalPrice,
          paymentInfo,
        });
        orders.push(order);
      }
      try {
        await sendMail({
          email: user.email,
          subject: "Order Successfull",
          message: `Hello ${user.name}, Your order is successfully placed and you can track your order from the Procart website. `,
        });
      } catch (error) {
        return next(new ErrorHandler(error.message, 500));
      }

      res.status(201).json({
        success: true,
        orders,
      });
    } catch (error) {
      return next(new ErrorHandler(error.message, 500));
    }
  })
);

